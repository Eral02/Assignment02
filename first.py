#area of triangle
base=float(input("enter base: "))
print(base)
heigh=float(input("enter heigh: ")) 
print(heigh)
area=float(input("enter area: "))
print(area)
area_of_triangle=area*base*heigh
print(area_of_triangle)

#area of square
width=int(input("enter width: "))
print(width)
length=int(input("enter length : ")) 
print(length)
area=width*length
print(area)

#area of circle
circle=float(input("enter circle: "))
print(circle)
radius=int(input("enter radius : ")) 
print(radius)
area_of_circle=circle*radius*radius
print(area_of_circle)

